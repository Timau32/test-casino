const selects = document.querySelectorAll('.select');

document.addEventListener('DOMContentLoaded', () => {
    selects.forEach(select => {

        const options = select.querySelector('.options');
        if (options) {
            const checkboxes = options.querySelectorAll('input[type="checkbox"]');
            select.addEventListener('click', (event) => {
                options.classList.toggle('open');
                event.stopPropagation();

                document.addEventListener('click', clickOutsideOptions);
            });

            const clickOutsideOptions = (event) => {

                if (!options.contains(event.target)) {
                    options.classList.remove('open');
                    document.removeEventListener('click', clickOutsideOptions);
                }
            };

            checkboxes.forEach(checkbox => {
                checkbox.addEventListener('click', () => {
                    if (checkbox.checked) {
                        if (!selectedCheckboxes.includes(checkbox.value)) {
                            selectedCheckboxes.push(checkbox.value);
                        }
                    } else {
                        const indexToRemove = selectedCheckboxes.indexOf(checkbox.value);
                        if (indexToRemove !== -1) {
                            selectedCheckboxes.splice(indexToRemove, 1);
                        }
                    }
                    console.log(selectedCheckboxes);
                    renderChecedFilters(selectedFilters, selectedCheckboxes)
                });
            });
        }
    });
});

