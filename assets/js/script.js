function renderOptions(container, array) {
    container.innerHTML = ''
    array.forEach(element => {
        container.innerHTML += `
        <div class="option_item">
         <label class="custom-checkbox">
            <input id="${element.label}" value="${element.label}" type="checkbox">
            <span class="checkbox-icon"></span>
            ${element.label}
          </label>
        </div>
        `
    });
}
function renderChecedFilters(container, array, checkboxes) {
    container.innerHTML = '';
    array.forEach(element => {
        container.innerHTML += `
            <label for="${element}" class="tag" data-value="${element.id}">
                ${element}
                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 16 16" fill="none">
                    <g opacity="0.6">
                        <path d="M5.17151 10.8281L10.8284 5.17127" stroke="black" stroke-width="1.5"
                            stroke-linecap="round" stroke-linejoin="round" />
                        <path d="M10.8285 10.8287L5.17163 5.17188" stroke="black" stroke-width="1.5"
                            stroke-linecap="round" stroke-linejoin="round" />
                    </g>
                </svg>
            </label>
        `;
    });

    const tags = container.querySelectorAll('.tag');
    tags.forEach(tag => {
        const svg = tag.querySelector('svg');
        svg.addEventListener('click', () => {
            const idToRemove = tag.dataset.value;
            const indexToRemove = array.findIndex(item => item.id === parseInt(idToRemove));
            if (indexToRemove !== -1) {
                array.splice(indexToRemove, 1);
                renderChecedFilters(container, array, checkboxes);
                const checkboxToDeactivate = checkboxes.find(checkbox => checkbox.value === idToRemove);
                if (checkboxToDeactivate) {
                    checkboxToDeactivate.checked = false;
                }
            }
        });

    });
}
